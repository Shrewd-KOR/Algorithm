N, K = input("N과 K를 입력하세요(공백기준)").split()
# n, k = map(int, input().split())
N = int(N)
K = int(K)
cnt = 0

while N != 1:
    if N % K == 0:
        cnt += 1
        N /= K
    else :
        cnt += 1
        N -= 1
    
print(cnt)

